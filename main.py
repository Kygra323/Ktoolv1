def dynamic_red_text(text, offset=0):
    import colorsys
    result = ""
    length = len(text)
    for i, char in enumerate(text):
        base_hue = ((i + offset) % length) / length
        hue = 0.92 + (0.16 * base_hue)
        hue %= 1.0
        r, g, b = colorsys.hsv_to_rgb(hue, 1, 1)
        color = f"\033[38;2;{int(r*255)};{int(g*255)};{int(b*255)}m"
        if char != "\n":
            result += f"{color}{char}\033[0m"
        else:
            result += "\n"
    return result

